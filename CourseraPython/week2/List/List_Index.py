# Метод index - возвращает индекс (позицию элемента) в последовательности
num_list = list(range(5))
print(num_list)  # [0, 1, 2, 3, 4]

print(num_list.index(2))  # 2
print(num_list.index(1, 0, 4))  # 1
print(num_list.index(6)) # ValueError: 6 is not in list







